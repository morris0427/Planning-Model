class TestResult:
    def __init__(self, name: str):
        self.name = name
        self.passed = 0
        self.failed = 0
        self.failures: List[str] = []

    def assert_(self, condition: bool, msg: str):
        if condition:
            self.passed += 1
        else:
            self.failed += 1
            self.failures.append(msg)

    def summary(self) -> str:
        status = "✓" if self.failed == 0 else "✗"
        return f"  {status} {self.name}: {self.passed} passed, {self.failed} failed"


