def test_ep_layer1_strict_roundtrip(num_problems: int = 3) -> TestResult:
    """Layer 1: Strict round-trip for 8-puzzle."""
    r = TestResult("8-puzzle Layer 1: strict round-trip (fresh problems)")
    try:
        from data.eight_puzzle import EightPuzzleDataset
    except Exception as e:
        r.assert_(False, f"could not import EightPuzzleDataset: {e}")
        return r

    for seed in range(num_problems):
        for use_wm in [False, True]:
            ds = EightPuzzleDataset(difficulty_range=(5, 10), num_samples=1,
                                     use_world_model=use_wm, seed=seed)
            problem = ds.generate_problem(difficulty=5 + seed)

            # Encode using OUR encoder (independent of the dataset class)
            seq = ep_encode_problem(problem, use_world_model=use_wm)
            # Decode it
            decoded = ep_decode_problem(seq, use_world_model=use_wm)

            r.assert_(
                np.array_equal(decoded['start_state'], problem['start_state']),
                f"seed={seed} wm={use_wm}: start_state mismatch"
            )
            r.assert_(
                np.array_equal(decoded['goal_state'], problem['goal_state']),
                f"seed={seed} wm={use_wm}: goal_state mismatch"
            )
            r.assert_(
                decoded['solution_moves'] == problem['solution_moves'],
                f"seed={seed} wm={use_wm}: solution_moves mismatch: "
                f"{decoded['solution_moves']} vs {problem['solution_moves']}"
            )
            r.assert_(
                decoded['num_moves'] == problem['num_moves'],
                f"seed={seed} wm={use_wm}: num_moves {decoded['num_moves']} vs {problem['num_moves']}"
            )
            # WM-only: state blocks must match the post-move trajectory
            if use_wm:
                r.assert_(
                    len(decoded['solution_states']) == problem['num_moves'],
                    f"seed={seed}: expected {problem['num_moves']} state blocks, "
                    f"got {len(decoded['solution_states'])}"
                )
                cur = problem['start_state'].copy()
                for k, mv in enumerate(problem['solution_moves']):
                    cur = ep_apply_move(cur, mv)
                    if k < len(decoded['solution_states']):
                        r.assert_(
                            np.array_equal(decoded['solution_states'][k], cur),
                            f"seed={seed}: state block {k} doesn't match post-move state "
                            f"(decoded={decoded['solution_states'][k].flatten().tolist()}, "
                            f"expected={cur.flatten().tolist()})"
                        )
    return r


def test_ep_layer3_dataset_consistency(num_problems: int = 3) -> TestResult:
    """Layer 3: The DATASET's own encode_sequence should match our reference encoder."""
    r = TestResult("8-puzzle Layer 3: dataset.encode_sequence matches reference")
    try:
        from data.eight_puzzle import EightPuzzleDataset
    except Exception as e:
        r.assert_(False, f"could not import EightPuzzleDataset: {e}")
        return r

    for seed in range(num_problems):
        for use_wm in [False, True]:
            ds = EightPuzzleDataset(difficulty_range=(5, 10), num_samples=1,
                                     use_world_model=use_wm, seed=seed)
            problem = ds.generate_problem(difficulty=5 + seed)
            their_seq = ds.encode_sequence(problem)
            our_seq = ep_encode_problem(problem, use_world_model=use_wm)
            r.assert_(
                their_seq == our_seq,
                f"seed={seed} wm={use_wm}: dataset encoder produces different sequence "
                f"than reference encoder.\n"
                f"      first 25 of dataset: {their_seq[:25]}\n"
                f"      first 25 of ours:    {our_seq[:25]}\n"
                f"      (if different at first state-block ~index 21, this is the "
                f"pre-move-vs-post-move bug)"
            )
    return r


def test_ep_layer4_cache_verification(cache_dir: str = 'cached_data') -> TestResult:
    """Layer 4: Verify cached 8-puzzle data is internally consistent."""
    r = TestResult("8-puzzle Layer 4: cached data internal consistency")
    cache_files = [
        (f'{cache_dir}/eight_puzzle_train_baseline.json', False),
        (f'{cache_dir}/eight_puzzle_test_baseline.json',  False),
        (f'{cache_dir}/eight_puzzle_train_wm.json',       True),
        (f'{cache_dir}/eight_puzzle_test_wm.json',        True),
    ]
    for path, use_wm in cache_files:
        if not Path(path).exists():
            print(f"      (skipping: {path} not present)")
            continue
        with open(path) as f:
            problems = json.load(f)
        print(f"      checking {path} ({len(problems)} problems, "
              f"WM={'yes' if use_wm else 'no'})")

        # Spot-check first 10 problems
        for i, p in enumerate(problems[:10]):
            seq = p['sequence']
            try:
                decoded = ep_decode_problem(seq, use_world_model=use_wm)
            except Exception as e:
                r.assert_(False, f"{path}[{i}]: decode failed: {e}")
                continue

            # The reference plan should reach the goal
            cur = decoded['start_state'].copy()
            for mv in decoded['solution_moves']:
                nxt = ep_apply_move(cur, mv)
                if nxt is None:
                    r.assert_(False, f"{path}[{i}]: invalid move '{mv}' in cached plan")
                    break
                cur = nxt
            else:
                r.assert_(
                    np.array_equal(cur, decoded['goal_state']),
                    f"{path}[{i}]: applying cached moves does NOT reach the goal. "
                    f"final={cur.flatten().tolist()}, goal={decoded['goal_state'].flatten().tolist()}"
                )

            # For WM: check whether state blocks are POST-move (current convention)
            # or PRE-move (the bug we found)
            if use_wm and len(decoded['solution_states']) > 0:
                cur = decoded['start_state'].copy()
                post_match = 0
                pre_match = 0
                for k, mv in enumerate(decoded['solution_moves']):
                    if k >= len(decoded['solution_states']):
                        break
                    nxt = ep_apply_move(cur, mv)
                    if nxt is None:
                        break
                    if np.array_equal(decoded['solution_states'][k], nxt):
                        post_match += 1
                    elif np.array_equal(decoded['solution_states'][k], cur):
                        pre_match += 1
                    cur = nxt
                n = len(decoded['solution_states'])
                if i == 0:  # print convention diagnosis for first problem only
                    if pre_match == n and post_match == 0:
                        print(f"        problem 0: ALL state blocks match PRE-move state "
                              f"(off-by-one bug confirmed)")
                    elif post_match == n and pre_match == 0:
                        print(f"        problem 0: ALL state blocks match POST-move state (correct)")
                    else:
                        print(f"        problem 0: mixed conventions "
                              f"(pre={pre_match}, post={post_match}, n={n})")
                # The check itself: all state blocks should match POST-move
                r.assert_(
                    post_match == n,
                    f"{path}[{i}]: state-block convention violation "
                    f"(pre-move matches: {pre_match}, post-move matches: {post_match}, n={n})"
                )
    return r


def test_bw_layer1_strict_roundtrip(num_problems: int = 3) -> TestResult:
    """Layer 1 (BW): strict, fully lossless round-trip.

    With the new uniform encoding the round-trip is now strict, not lossy.
    encode(state) then decode_state(...) should recover state byte-for-byte.
    """
    r = TestResult("BW Layer 1: strict round-trip (fresh problems)")
    try:
        from data.blocks_world import BlocksWorldDataset
    except Exception as e:
        r.assert_(False, f"could not import BlocksWorldDataset: {e}")
        return r

    for seed in range(num_problems):
        for use_wm in [False, True]:
            ds = BlocksWorldDataset(difficulty_range=(3, 5), num_samples=1,
                                     use_world_model=use_wm, seed=seed)
            problem = ds.generate_problem(difficulty=3 + seed)

            # Reference encode (our independent implementation), then decode
            seq = bw_encode_problem(problem, use_world_model=use_wm)
            decoded = bw_decode_problem(seq, use_world_model=use_wm)

            r.assert_(
                decoded['start_state'] == problem['start_state'],
                f"seed={seed} wm={use_wm}: start_state mismatch "
                f"(decoded={decoded['start_state']}, expected={problem['start_state']})"
            )
            r.assert_(
                decoded['goal_state'] == problem['goal_state'],
                f"seed={seed} wm={use_wm}: goal_state mismatch "
                f"(decoded={decoded['goal_state']}, expected={problem['goal_state']})"
            )
            r.assert_(
                decoded['solution_moves'] == problem['solution_moves'],
                f"seed={seed} wm={use_wm}: solution_moves mismatch: "
                f"decoded={decoded['solution_moves']} expected={problem['solution_moves']}"
            )
            r.assert_(
                decoded['num_moves'] == problem['num_moves'],
                f"seed={seed} wm={use_wm}: num_moves mismatch"
            )

            # Uniform-length check: each state block should be exactly 8 tokens.
            # We can verify by reconstructing: every state block is 8 tokens.
            if use_wm:
                # The encoder pairs move i with the POST-move state. Verify the
                # decoded intermediate states match what we get by applying moves.
                cur = [t[:] for t in problem['start_state']]
                for k, mv in enumerate(problem['solution_moves']):
                    cur = bw_apply_action(cur, mv)
                    if k < len(decoded['solution_states']):
                        r.assert_(
                            decoded['solution_states'][k] == cur,
                            f"seed={seed}: post-move state {k} mismatch "
                            f"(decoded={decoded['solution_states'][k]}, expected={cur})"
                        )
                # Number of state blocks should equal number of moves
                r.assert_(
                    len(decoded['solution_states']) == problem['num_moves'],
                    f"seed={seed}: expected {problem['num_moves']} state blocks, "
                    f"got {len(decoded['solution_states'])}"
                )
    return r


