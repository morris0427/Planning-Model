# BLOCKS WORLD encoding/decoding
# ============================================================================

BW_VOCAB = {
    'START': 0, 'END': 1,
    'A': 2, 'B': 3, 'C': 4, 'D': 5,
    'POS_0': 6, 'POS_1': 7, 'POS_2': 8, 'POS_3': 9,
    'PAD': 10,
}
BW_INV = {v: k for k, v in BW_VOCAB.items()}
BW_BLOCKS = ['A', 'B', 'C', 'D']
BW_NUM_POSITIONS = 4
BW_START = 0
BW_END = 1
BW_STATE_LEN = 8       # uniform: 4 blocks + 4 POS_k separators always present
BW_ACTION_LEN = 2      # [block, POS_k]
BW_CONTEXT_END = 1 + BW_STATE_LEN + BW_STATE_LEN   # 17: START + start_state(8) + goal_state(8)
BW_POS_TOKENS = {BW_VOCAB[f'POS_{k}']: k for k in range(BW_NUM_POSITIONS)}
BW_BLOCK_TOKENS = {BW_VOCAB[b] for b in BW_BLOCKS}


def bw_apply_action(state: List[List[str]], action: List) -> Optional[List[List[str]]]:
    """Apply [block, dest_pos] to state. Returns new state or None if invalid."""
    block, dest_pos = action
    source_pos = None
    for pos_idx, tower in enumerate(state):
        if tower and tower[-1] == block:
            source_pos = pos_idx
            break
    if source_pos is None:
        return None
    new_state = [t[:] for t in state]
    new_state[source_pos].pop()
    new_state[dest_pos].append(block)
    return new_state


def bw_encode_state(state: List[List[str]]) -> List[int]:
    """Uniform 8-token state encoding: top-to-bottom within each tower,
    POS_k separator always emitted (even for empty positions).

    state [['A','B','C'], [], ['D'], []]
        -> [C, B, A, POS_0, POS_1, D, POS_2, POS_3]
    """
    tokens = []
    for pos_idx, tower in enumerate(state):
        for block in reversed(tower):  # top-to-bottom
            tokens.append(BW_VOCAB[block])
        tokens.append(BW_VOCAB[f'POS_{pos_idx}'])
    assert len(tokens) == BW_STATE_LEN, (
        f"state encoding has {len(tokens)} tokens, expected {BW_STATE_LEN}. "
        f"State: {state}"
    )
    return tokens


def bw_decode_state(tokens: List[int]) -> List[List[str]]:
    """Inverse of bw_encode_state. Returns a state in bottom-to-top raw format."""
    if len(tokens) != BW_STATE_LEN:
        raise ValueError(
            f"bw_decode_state: expected {BW_STATE_LEN} tokens, got {len(tokens)}"
        )
    state: List[List[str]] = [[] for _ in range(BW_NUM_POSITIONS)]
    buf: List[str] = []
    for tok in tokens:
        if tok in BW_BLOCK_TOKENS:
            buf.append(BW_INV[tok])
        elif tok in BW_POS_TOKENS:
            state[BW_POS_TOKENS[tok]] = list(reversed(buf))   # top-to-bottom -> bottom-to-top
            buf = []
        else:
            raise ValueError(
                f"bw_decode_state: unexpected token {tok} ({BW_INV.get(tok, '?')})"
            )
    if buf:
        raise ValueError(
            f"bw_decode_state: trailing tokens with no closing POS_k: {buf}"
        )
    return state


def bw_decode_problem(seq: List[int], use_world_model: bool) -> Dict[str, Any]:
    """Decode a Blocks World token sequence into a structured problem dict.

    Uniform layout (use_world_model=True):
      [START=0, start_state(8), goal_state(8),
       for each move m_k: m_k(2 tokens), state_k(8),
       END=1]
    Layout (use_world_model=False):
      [START=0, start_state(8), goal_state(8),
       move_1(2), move_2(2), ..., move_N(2),
       END=1]

    Returns dict with:
      start_state, goal_state             (full semantic states, lossless)
      solution_moves                       (list of [block_str, pos_int])
      solution_states                      (list of full semantic states, one per emitted state block)
      num_moves
    """
    if len(seq) < BW_CONTEXT_END + 1:
        raise ValueError(f"sequence too short: len={len(seq)}")
    if seq[0] != BW_START:
        raise ValueError(f"position 0 is {seq[0]}, expected START={BW_START}")
    if seq[-1] != BW_END:
        raise ValueError(f"last token is {seq[-1]}, expected END={BW_END}")

    start_state = bw_decode_state(seq[1:1 + BW_STATE_LEN])
    goal_state = bw_decode_state(seq[1 + BW_STATE_LEN:BW_CONTEXT_END])

    moves: List[List] = []
    states: List[List[List[str]]] = []
    pos = BW_CONTEXT_END
    while pos < len(seq) - 1:  # leave room for END
        # Action block (2 tokens)
        if pos + BW_ACTION_LEN > len(seq) - 1 and seq[pos] == BW_END:
            break
        if pos + BW_ACTION_LEN > len(seq):
            raise ValueError(f"position {pos}: ran out of tokens reading action")
        block_tok, pos_tok = seq[pos], seq[pos + 1]
        if block_tok not in BW_BLOCK_TOKENS:
            raise ValueError(
                f"position {pos}: expected block token, got {block_tok} ({BW_INV.get(block_tok)})"
            )
        if pos_tok not in BW_POS_TOKENS:
            raise ValueError(
                f"position {pos+1}: expected POS_ token, got {pos_tok} ({BW_INV.get(pos_tok)})"
            )
        moves.append([BW_INV[block_tok], BW_POS_TOKENS[pos_tok]])
        pos += BW_ACTION_LEN

        if not use_world_model:
            continue

        # State block (8 tokens) -- always present after each action in WM
        if pos < len(seq) - 1 and seq[pos] != BW_END:
            if pos + BW_STATE_LEN > len(seq):
                raise ValueError(
                    f"position {pos}: expected {BW_STATE_LEN}-token state block "
                    f"but only {len(seq) - pos} tokens remain"
                )
            states.append(bw_decode_state(seq[pos:pos + BW_STATE_LEN]))
            pos += BW_STATE_LEN

    return {
        'start_state': start_state,
        'goal_state': goal_state,
        'solution_moves': moves,
        'solution_states': states,   # post-move states, one per move (in WM mode)
        'num_moves': len(moves),
    }


def bw_encode_problem(problem: Dict[str, Any], use_world_model: bool) -> List[int]:
    """Encode a Blocks World problem with the uniform lossless encoding.

    Pairs move i with the POST-move state, derived by walking forward from
    start with apply_action. We do NOT rely on problem['solution_states'] here
    -- this is the reference encoder, intentionally independent of however the
    dataset class structures things.
    """
    seq: List[int] = [BW_START]
    seq.extend(bw_encode_state(problem['start_state']))
    seq.extend(bw_encode_state(problem['goal_state']))

    if use_world_model:
        cur = [t[:] for t in problem['start_state']]
        for move in problem['solution_moves']:
            seq.append(BW_VOCAB[move[0]])
            seq.append(BW_VOCAB[f"POS_{move[1]}"])
            cur = bw_apply_action(cur, move)
            if cur is None:
                raise ValueError(f"invalid move {move} while encoding")
            seq.extend(bw_encode_state(cur))
    else:
        for move in problem['solution_moves']:
            seq.append(BW_VOCAB[move[0]])
            seq.append(BW_VOCAB[f"POS_{move[1]}"])

    seq.append(BW_END)
    return seq


