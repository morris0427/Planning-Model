def main():
    ap = argparse.ArgumentParser()
    ap.add_argument('--domain', choices=['eight_puzzle', 'blocks_world', 'both'],
                    default='both')
    ap.add_argument('--num-fresh', type=int, default=3,
                    help='Number of freshly generated problems per (seed, wm) cell')
    ap.add_argument('--cache-dir', default='cached_data')
    ap.add_argument('--skip-cache', action='store_true',
                    help='Skip Layer 4 cache verification')
    args = ap.parse_args()

    print("=" * 76)
    print("ENCODING ROUND-TRIP TEST SUITE")
    print("=" * 76)

    results: List[TestResult] = []

    if args.domain in ('eight_puzzle', 'both'):
        print("\n--- 8-PUZZLE ---")
        results.append(test_ep_layer1_strict_roundtrip(args.num_fresh))
        results.append(test_ep_layer3_dataset_consistency(args.num_fresh))
        if not args.skip_cache:
            results.append(test_ep_layer4_cache_verification(args.cache_dir))

    if args.domain in ('blocks_world', 'both'):
        print("\n--- BLOCKS WORLD ---")
        results.append(test_bw_layer1_strict_roundtrip(args.num_fresh))
        results.append(test_bw_layer3_dataset_consistency(args.num_fresh))
        if not args.skip_cache:
            results.append(test_bw_layer4_cache_verification(args.cache_dir))

    print("\n" + "=" * 76)
    print("SUMMARY")
    print("=" * 76)
    total_passed = sum(r.passed for r in results)
    total_failed = sum(r.failed for r in results)
    for r in results:
        print(r.summary())
        for msg in r.failures[:10]:  # cap noise
            print(f"      - {msg}")
        if len(r.failures) > 10:
            print(f"      ... and {len(r.failures) - 10} more")
    print("-" * 76)
    print(f"TOTAL: {total_passed} passed, {total_failed} failed")
    print("=" * 76)

    sys.exit(0 if total_failed == 0 else 1)


if __name__ == '__main__':
    main()
