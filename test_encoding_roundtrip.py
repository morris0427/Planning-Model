"""
Encoding round-trip tests for 8-Puzzle and Blocks World datasets.

WHY THIS EXISTS
---------------
Every bug we've found in this experiment has been an encoding issue:
  - 8-puzzle cache stores PRE-move state instead of POST-move state
  - Blocks World encoder reuses POS_0 as padding for empty towers (ambiguous)
  - 8-puzzle action tokens use IDs 10-13 in data, but the trainer constants
    used to claim 9-12 (since fixed, but caused confusion for hours)
  - Cache files on disk were sometimes generated by older scripts that no
    longer exist in the repo, with conventions different from the current
    encoder code
This suite catches these systematically instead of one-by-one through
downstream failures.

LAYERS OF TESTING
-----------------
Layer 1: Strict round-trip
    raw_problem -> encode -> decode -> raw_problem'
    Check raw_problem == raw_problem' (where possible)

Layer 2: Lossy round-trip (Blocks World)
    Acknowledge that Blocks World state encoding loses information.
    Verify only what should be preserved: top-blocks-by-position view.
    Document what's lost.

Layer 3: Generator-to-encoder consistency on FRESHLY GENERATED problems
    For a newly generated problem, encode it, decode it, and verify:
      - The decoded action sequence equals the generated action sequence
      - Applying the decoded actions to the decoded start state reaches the
        decoded goal state (the decoded plan is valid)
      - Encoded state-blocks correspond to actual intermediate states under
        the post-move convention

Layer 4: Cache verification (CRITICAL)
    For each CACHED problem on disk, decode and check internal consistency.
    This catches caches generated by old scripts that no longer match the
    current encoder. This is the test we wish we'd had from the start.

USAGE
-----
    python3 test_encoding_roundtrip.py
    python3 test_encoding_roundtrip.py --domain eight_puzzle
    python3 test_encoding_roundtrip.py --domain blocks_world
    python3 test_encoding_roundtrip.py --skip-cache    # if no cache files exist
    python3 test_encoding_roundtrip.py --num-fresh 5   # check N freshly generated
"""

import argparse
import json
import sys
from copy import deepcopy
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

import numpy as np


# ============================================================================
# 8-PUZZLE encoding/decoding
# ============================================================================

EP_MOVE_TO_TOKEN = {'up': 10, 'down': 11, 'left': 12, 'right': 13}
EP_TOKEN_TO_MOVE = {v: k for k, v in EP_MOVE_TO_TOKEN.items()}
EP_SEP = 14
EP_PAD = 15
EP_DUMMY = 13  # 'right' used as the leading dummy token


def ep_apply_move(state: np.ndarray, move: str) -> Optional[np.ndarray]:
    """Apply a single move to a 3x3 state. Returns new state or None if invalid."""
    s = state.copy()
    pos = np.argwhere(s == 0)
    if len(pos) == 0:
        return None
    r, c = pos[0]
    if move == 'up' and r > 0:
        s[r, c], s[r-1, c] = s[r-1, c], s[r, c]
    elif move == 'down' and r < 2:
        s[r, c], s[r+1, c] = s[r+1, c], s[r, c]
    elif move == 'left' and c > 0:
        s[r, c], s[r, c-1] = s[r, c-1], s[r, c]
    elif move == 'right' and c < 2:
        s[r, c], s[r, c+1] = s[r, c+1], s[r, c]
    else:
        return None
    return s


def ep_decode_problem(seq: List[int], use_world_model: bool) -> Dict[str, Any]:
    """Decode an 8-puzzle token sequence into a structured problem dict.

    Layout (use_world_model=True):
      [dummy=13, start(9), PAD=15, goal(9),
       for each move m_k:  m_k, state_k(9),
       SEP=14]
    Layout (use_world_model=False):
      [dummy=13, start(9), PAD=15, goal(9),
       move_1, move_2, ..., move_N,
       SEP=14]

    Returns dict with: start_state (3x3 array), goal_state (3x3 array),
    solution_moves (list of strings), solution_states (list of 3x3 arrays,
    one per emitted state block in the sequence), num_moves (int).

    Does NOT verify validity -- that's a separate test.
    """
    if len(seq) < 21:
        raise ValueError(f"sequence too short: len={len(seq)}")
    if seq[0] != EP_DUMMY:
        raise ValueError(f"position 0 is {seq[0]}, expected dummy={EP_DUMMY}")
    if seq[10] != EP_PAD:
        raise ValueError(f"position 10 is {seq[10]}, expected PAD={EP_PAD}")
    if seq[-1] != EP_SEP:
        raise ValueError(f"last token is {seq[-1]}, expected SEP={EP_SEP}")

    start = np.array(seq[1:10]).reshape(3, 3)
    goal = np.array(seq[11:20]).reshape(3, 3)

    moves: List[str] = []
    states: List[np.ndarray] = []
    pos = 20
    while pos < len(seq) - 1:  # leave room for SEP
        tok = seq[pos]
        if tok not in EP_TOKEN_TO_MOVE:
            raise ValueError(
                f"position {pos}: token {tok} is not a move token "
                f"(expected one of {sorted(EP_TOKEN_TO_MOVE)})"
            )
        moves.append(EP_TOKEN_TO_MOVE[tok])
        pos += 1
        if use_world_model:
            if pos + 9 > len(seq) - 1:
                raise ValueError(
                    f"position {pos}: WM expected 9 state tokens before SEP, "
                    f"but only {len(seq) - 1 - pos} remain"
                )
            state = np.array(seq[pos:pos+9]).reshape(3, 3)
            states.append(state)
            pos += 9

    return {
        'start_state': start,
        'goal_state': goal,
        'solution_moves': moves,
        'solution_states': states,  # may be empty for baseline
        'num_moves': len(moves),
    }


def ep_encode_problem(problem: Dict[str, Any], use_world_model: bool) -> List[int]:
    """Encode an 8-puzzle problem the way the current code SHOULD encode it.

    Uses post-move-state convention (which is what we want, and what we'll
    compare cached data against). This is independent of the dataset class.
    """
    seq: List[int] = [EP_DUMMY]
    seq.extend(problem['start_state'].flatten().tolist())
    seq.append(EP_PAD)
    seq.extend(problem['goal_state'].flatten().tolist())

    if use_world_model:
        cur = problem['start_state'].copy()
        for mv in problem['solution_moves']:
            seq.append(EP_MOVE_TO_TOKEN[mv])
            cur = ep_apply_move(cur, mv)
            if cur is None:
                raise ValueError(f"invalid move '{mv}' during encoding")
            seq.extend(cur.flatten().tolist())
    else:
        for mv in problem['solution_moves']:
            seq.append(EP_MOVE_TO_TOKEN[mv])

    seq.append(EP_SEP)
    return seq


